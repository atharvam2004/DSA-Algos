class Solution
{
    Node compute(Node head)
    {   
       Node revhead=reverse(head);
       int max=revhead.data;
       Node temp=revhead.next;
       Node prev=revhead;
       while(temp!=null){
           if(max<=temp.data){
               prev.next=temp;
               prev=prev.next;
               max=temp.data;
           }
           temp=temp.next;
       }
         prev.next=temp;
       head=reverse(revhead);
       return head;
    }
    Node reverse(Node node)
    {
        Node prev = null;
        Node current = node;
        Node next = null;
        while (current != null) {
            next = current.next;
            current.next = prev;
            prev = current;
            current = next;
        }
        node = prev;
        return node;
        
    }
}
  